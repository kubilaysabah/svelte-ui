<script lang="ts">
	import { Popover as PopoverPrimitive } from "bits-ui";
	import type { Snippet } from "svelte";

	interface Props {
		open?: boolean;
		onOpenChange?: (open: boolean) => void;
		children?: Snippet;
	}

	let {
		open = $bindable(false),
		onOpenChange,
		children,
		...restProps
	}: Props = $props();
</script>

<PopoverPrimitive.Root
	bind:open
	{onOpenChange}
	{...restProps}
>
	{@render children?.()}
</PopoverPrimitive.Root>
